                            summit reveals intricately landscaped gardens, advanced water management systems, and the
                            famous frescoes of celestial maidens, painted on the rock walls. At the summit, the ruins of
                            the king's palace offer a glimpse into the ingenuity of ancient Sri Lankan architecture,
                            while the surrounding views of forests and villages are breathtaking. Visitors also
                            encounter the Mirror Wall, once so polished that the king could see his reflection.
